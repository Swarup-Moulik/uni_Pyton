num = int(input("Enter the day as number :- "))
if num == 1:
    print("It is Sunday.")
elif num == 2:
    print("It is Monday.")
elif num == 3:
    print("It is Tuesday.")
elif num == 4:
    print("It is Wednesday.")
elif num == 5:
    print("It is Thursday.")
elif num == 6:
    print("It is Friday.")
elif num == 7:
    print("It is Saturday.")
else:
    print("Wrong day number.")